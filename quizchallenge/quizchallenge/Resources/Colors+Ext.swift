import UIKit

extension UIColor {

    class var arcGrey: UIColor {
        return UIColor(red: 245.0 / 255.0, green: 245.0 / 255.0, blue: 245.0 / 255.0, alpha: 1.0)
    }

    class var arcOrange: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 131.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0)
    }

    class var darkOrange: UIColor {
        return UIColor(red: 200.0 / 255.0, green: 100.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0)
    }
}
